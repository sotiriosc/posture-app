const pickSwapOptions = (exercise: Exercise, list: Exercise[]) => {
  const matches = list.filter((candidate) => {
    if (candidate.id === exercise.id) return false;
    const patternOverlap = candidate.movementPattern.some((tag) =>
      exercise.movementPattern.includes(tag)
    );
    const tagOverlap = candidate.tags.some((tag) =>
      exercise.tags.includes(tag)
    );
    const loadCompatible =
      candidate.loadType === exercise.loadType ||
      (exercise.loadType !== "weighted" && candidate.loadType !== "weighted");
    return (patternOverlap || tagOverlap) && loadCompatible;
  });
  return matches.slice(0, 5).map((item) => item.id);
};

const normalizeTagToken = (value: string) =>
  value
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "");

const inferPatternFromExercise = (exercise: Exercise): Exercise["pattern"] => {
  if (exercise.pattern) return exercise.pattern;
  if (exercise.category !== "main") return undefined;

  const movementTokens = new Set(
    exercise.movementPattern.map((item) => normalizeTagToken(item))
  );
  const descriptor = normalizeTagToken(`${exercise.id} ${exercise.name}`);
  const hasMovement = (...tokens: string[]) =>
    tokens.some((token) => movementTokens.has(normalizeTagToken(token)));
  const textHasAny = (...tokens: string[]) =>
    tokens.some((token) => descriptor.includes(normalizeTagToken(token)));

  if (
    hasMovement("kneedominant", "squat", "lunge") ||
    textHasAny("squat", "lunge", "stepup", "split squat", "cossack", "pistol", "shrimp")
  ) {
    return "knee_dominant";
  }

  if (
    hasMovement("hinge") ||
    textHasAny("rdl", "deadlift", "hinge", "good morning", "hip thrust", "back extension")
  ) {
    return "hinge";
  }

  if (
    hasMovement("core", "antirotation", "antiextension", "carry") ||
    textHasAny("plank", "dead bug", "hollow", "pallof", "woodchop", "carry", "rollout")
  ) {
    return "core_stability";
  }

  if (hasMovement("verticalpush")) return "vertical_push";
  if (hasMovement("horizontalpush")) return "horizontal_push";
  if (hasMovement("verticalpull")) return "vertical_pull";
  if (hasMovement("horizontalpull")) return "horizontal_pull";

  if (hasMovement("push")) {
    if (textHasAny("overhead", "shoulder press", "pike", "arnold", "strict press", "push press")) {
      return "vertical_push";
    }
    return "horizontal_push";
  }

  if (hasMovement("pull")) {
    if (textHasAny("pullup", "chinup", "pulldown", "lat", "vertical")) {
      return "vertical_pull";
    }
    return "horizontal_pull";
  }

  if (textHasAny("biceps", "curl")) return "horizontal_pull";
  if (textHasAny("triceps", "pressdown", "extension")) return "horizontal_push";
  if (textHasAny("calf", "calves", "ankle")) return "knee_dominant";

  return undefined;
};

const clampDifficulty = (value: number): 1 | 2 | 3 | 4 | 5 =>
  Math.max(1, Math.min(5, Math.round(value))) as 1 | 2 | 3 | 4 | 5;

const inferDifficultyForExercise = (
  exercise: Exercise,
  pattern?: Exercise["pattern"]
): Exercise["difficulty"] => {
  if (exercise.difficulty) return exercise.difficulty;
  if (exercise.category !== "main") return undefined;

  const descriptor = normalizeTagToken(`${exercise.id} ${exercise.name} ${exercise.tags.join(" ")}`);
  let score = 2;

  if (exercise.loadType === "weighted") score += 1;
  if (pattern === "vertical_pull" || pattern === "vertical_push") score += 1;
  if (pattern === "core_stability" && exercise.loadType === "timed") score += 1;
  if (pattern === "knee_dominant" && descriptor.includes("singleleg")) score += 1;
  if (
    ["advanced", "archer", "pike", "pistol", "shrimp", "chinup", "pullup", "strictpress", "rollout"].some(
      (token) => descriptor.includes(token)
    )
  ) {
    score += 1;
  }
  if (["drill", "march", "activation"].some((token) => descriptor.includes(token))) {
    score -= 1;
  }

  return clampDifficulty(score);
};

const inferPainContraindicationsForExercise = (
  exercise: Exercise,
  pattern?: Exercise["pattern"]
): Exercise["painContraindications"] => {
  if (exercise.painContraindications?.length) return exercise.painContraindications;
  if (exercise.category !== "main") return undefined;

  const byPattern: Record<string, string[]> = {
    horizontal_pull: ["shoulders", "elbows", "neck"],
    vertical_pull: ["shoulders", "elbows", "neck"],
    horizontal_push: ["shoulders", "wrists", "elbows"],
    vertical_push: ["shoulders", "wrists", "elbows"],
    knee_dominant: ["knees", "hips", "ankles"],
    hinge: ["low back", "hamstrings", "hips"],
    core_stability: ["low back", "shoulders", "neck"],
  };
  if (pattern && byPattern[pattern]) return byPattern[pattern];

  const descriptor = normalizeTagToken(`${exercise.id} ${exercise.name}`);
  if (
    ["squat", "lunge", "stepup", "splitsquat", "cossack", "pistol", "shrimp"].some((token) =>
      descriptor.includes(token)
    )
  ) {
    return byPattern.knee_dominant;
  }
  if (["rdl", "deadlift", "hinge", "goodmorning", "hipthrust", "backextension"].some((token) => descriptor.includes(token))) {
    return byPattern.hinge;
  }
  if (["pullup", "pulldown", "row", "pull", "facepull"].some((token) => descriptor.includes(token))) {
    return byPattern.horizontal_pull;
  }
  if (["press", "pushup", "push", "fly"].some((token) => descriptor.includes(token))) {
    return byPattern.horizontal_push;
  }
  if (["plank", "deadbug", "hollow", "pallof", "carry", "woodchop", "rollout"].some((token) => descriptor.includes(token))) {
    return byPattern.core_stability;
  }

  return undefined;
};

const inferDifficultyTierForExercise = (params: {
  exercise: Exercise;
  difficulty?: Exercise["difficulty"];
}): Exercise["difficultyTier"] => {
  const { exercise, difficulty } = params;
  if (exercise.difficultyTier) return exercise.difficultyTier;
  if (exercise.category !== "main") return undefined;

  const descriptor = normalizeTagToken(`${exercise.id} ${exercise.name} ${exercise.tags.join(" ")}`);
  const hasAdvancedMarker =
    descriptor.includes("advanced") ||
    descriptor.includes("pistol") ||
    descriptor.includes("shrimp") ||
    descriptor.includes("archer") ||
    descriptor.includes("strictpress") ||
    descriptor.includes("chesttobarpullup");

  if ((difficulty ?? 3) <= 2) return "easy";
  if ((difficulty ?? 3) >= 5 && hasAdvancedMarker) return "hard";
  return "moderate";
};

const inferMovementIntensityForExercise = (params: {
  exercise: Exercise;
  pattern?: Exercise["pattern"];
}): Exercise["movementIntensity"] => {
  const { exercise, pattern } = params;
  if (exercise.movementIntensity) return exercise.movementIntensity;
  if (exercise.category !== "main") return undefined;

  const movementTokens = new Set(
    exercise.movementPattern.map((item) => normalizeTagToken(item))
  );
  const descriptor = normalizeTagToken(`${exercise.id} ${exercise.name} ${exercise.tags.join(" ")}`);
  const hasMobilitySignal =
    movementTokens.has("mobility") ||
    descriptor.includes("mobility") ||
    descriptor.includes("stretch") ||
    descriptor.includes("thoracic");
  if (hasMobilitySignal) return "mobility";

  // Keep hinge default as pattern intensity so existing hinge safety behavior
  // remains stable while still normalizing metadata completeness.
  if (pattern === "hinge") return "pattern";

  if (exercise.loadType === "weighted") return "load";
  return "pattern";
};

export const exercises: Exercise[] = rawExercises.map((exercise) => {
  const pattern = inferPatternFromExercise(exercise);
  const difficulty = inferDifficultyForExercise(exercise, pattern);
  const painContraindications = inferPainContraindicationsForExercise(
    exercise,
    pattern
  );
  const difficultyTier = inferDifficultyTierForExercise({ exercise, difficulty });
  const movementIntensity = inferMovementIntensityForExercise({ exercise, pattern });

  return {
    ...exercise,
    pattern,
    difficulty,
    difficultyTier,
    movementIntensity,
    painContraindications,
    focusTags: exercise.focusTags ?? exercise.tags ?? [],
    swapOptions:
      exercise.swapOptions && exercise.swapOptions.length
        ? exercise.swapOptions
        : pickSwapOptions(exercise, rawExercises),
  };
});

const normalizeId = (id?: string) => (id ?? "").trim().toLowerCase();

export const exerciseById = (id?: string) => {
  const normalized = normalizeId(id);
  if (!normalized) return undefined;
  return exercises.find((exercise) => normalizeId(exercise.id) === normalized);
};

